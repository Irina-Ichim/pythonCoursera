# clase_ejemplo.py

# Definición de la clase Apple
class Apple:
    color = 'unknown'
    flavor = 'unknown'

# Creación de instancias de la clase Apple
jonagold = Apple()
jonagold.color = 'red'
jonagold.flavor = 'sweet'

granny_smith = Apple()
granny_smith.color = 'green'
granny_smith.flavor = 'tart'

# Impresión de información sobre las instancias
print("Información sobre la manzana Jonagold:")
print("Color:", jonagold.color)
print("Sabor:", jonagold.flavor)
print()

print("Información sobre la manzana Granny Smith:")
print("Color:", granny_smith.color)
print("Sabor:", granny_smith.flavor)
