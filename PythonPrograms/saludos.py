"""
Docstring: Operador de Módulo en Python

El operador de módulo (%) en Python se utiliza para calcular el residuo de una división.
Cuando se utiliza la expresión 'a % b', Python calcula el residuo de dividir 'a' entre 'b'.

Ejemplo:

Si tenemos la expresión '20 % 100', significa que estamos calculando
el residuo de dividir 20 entre 100.

El resultado de '20 % 100' es 20, porque 20 no es divisible
exactamente por 100 y el residuo es 20.

En resumen, el operador de módulo (%) es útil para obtener el residuo de una división y
es comúnmente utilizado en matemáticas y programación.

Para calcular el radical √ en Python usa esta formula : x**(1/2) osea (x**0.05)
"""

